#include <bits/stdc++.h>
using namespace std;

int main() {
  int A, B;
  cin >> A >> B;// 合計点を表す変数
  int i = 0;   // カウンタ変数
  //start count A
  cout << "A:";
  while (i < A) {
    cout << "]";
    i++;
  }
  cout << endl;
  i = 0; // i reset
  //start count B
  cout << "B:";
  while (i < B) {
    cout << "]";
    i++;
  }
}



